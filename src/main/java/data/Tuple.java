package data;

public class Tuple {

    private double x;
    private double y;
    private double output;

    public Tuple(double x, double y, double output) {
        this.x = x;
        this.y = y;
        this.output = output;
    }

    public double getX() {
        return x;
    }

    public double getY() {
        return y;
    }

    public double getOutput() {
        return output;
    }
}
